public class UnionCheck {
    public String id;

    public String name;

    public String type;

    public Long allmember;

    public Long offical;

    public Long needcon;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Long getAllmember() {
        return allmember;
    }

    public void setAllmember(Long allmember) {
        this.allmember = allmember;
    }

    public Long getOffical() {
        return offical;
    }

    public void setOffical(Long offical) {
        this.offical = offical;
    }

    public Long getNeedcon() {
        return needcon;
    }

    public void setNeedcon(Long needcon) {
        this.needcon = needcon;
    }
}
